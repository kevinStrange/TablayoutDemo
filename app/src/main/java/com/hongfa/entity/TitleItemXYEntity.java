package com.hongfa.entity;

/**
 * 用于存储每个导航栏的x y轴
 */
public class TitleItemXYEntity {
    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
